    private void tabelaMercadoKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_tabelaMercadoKeyPressed
        //Verificando se foi pressionado a tecla delete
        if (evt.getKeyCode() == java.awt.event.KeyEvent.VK_DELETE) {
            //Verificando se tem uma linha para apagar
            if (tabelaMercado.getSelectedRow() == -1) {
                JOptionPane.showMessageDialog(null, "Selecione uma linha para apagar");
            } else {
                //Pegando o codigo da linha selecionada
                Object obj = tabelaMercado.getValueAt(tabelaMercado.getSelectedRow(), 0);
                int conf = JOptionPane.showConfirmDialog(null, "Deseja apagar este produto?");
                if (conf == 0) {
                    boolean resultado = pDAO.deletar(obj.toString());
                    if (resultado == true) {
                        modeloTabela.atualizar();
                        lista = pDAO.listar();
                        modeloTabela.adicionarLinha(lista);
                    }
                }
            }
        }
    }//GEN-LAST:event_tabelaMercadoKeyPressed

